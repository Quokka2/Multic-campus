package 반복문;

public class For3 {

	public static void main(String[] args) {

		for (int i = 0; i < 10; i++) {System.out.print("*");} // 옆으로 출력하려면 'ln'을 빼면된다			
		System.out.println();//밑으로 엔터기능 하고싶으면 사이에 이거 넣으면 됨
		for (int i = 0; i < 5; i++) {System.out.print("커피");}
		System.out.println();
		for (int i = 0; i < 3; i++) {System.out.println("커피*우유");}
		for (int i = 1; i < 4; i++) {System.out.println(i + " : 짱!");}
		}

}
