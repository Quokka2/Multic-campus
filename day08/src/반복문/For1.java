
package 반복문;

public class For1 {
	
		public static void main(String[] args) {
			
			for (int i = 0; i < 3; i++) {
				System.out.println(i + "Hi");
			}
			for (int i = 0; i < 100; i++) {
				System.out.println("Hi2");
			}
			for (int i = 1; i < 11; i++) {
				System.out.println(i + " Hi3");
			}
		}
}
