package 조건문;

public class IF문1 {
	public static void main(String[] args) {
		int x = 0;
		
		
		if (x == 0) {
			System.out.println("내가 실행되겠지요??");
		}else { // false일 때 처리하고 싶은 내용을 쓰면 됨
			System.out.println("나는 조건이 맞지 않을 때 실행되는거야.");
		}
		
		if (x == 1) {
			System.out.println("나는 실행될까요????");
		}
	}
}
