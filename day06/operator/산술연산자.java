package operator;

public class 산술연산자 {

	public static void main(String[] args) {
		// 산술연산자(사칙연산자) % = 나머지 구하는 연산자
		int x = 20;
		int y = 10;
		
		
		System.out.println("연산");
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);

	}

}
